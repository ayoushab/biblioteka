    private void loginBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginBtnActionPerformed
  
      try {
       con = _db.ConnectBase();
       String sql = "SELECT * FROM `usernames` WHERE `username`='"+userTxt.getText().trim()+"' and `password`='"+String.valueOf(pswFld.getPassword())+"'";
       PreparedStatement pst = con.prepareStatement(sql);
       ResultSet rs = pst.executeQuery();
       if(rs.next()) {
           //JOptionPane.showMessageDialog(null, "Ulogovali ste se!");
           Main frm = new Main();
           frm.setVisible(true);
           setVisible(false);
       }
       else{
           JOptionPane.showMessageDialog(null, "Username ili Password nisu ispravni ili Niste uspostavili konekciju sa bazom!");
           userTxt.setText("");
           pswFld.setText("");
       }
       }
       catch(Exception e)
       {
                  JOptionPane.showMessageDialog(null, "Gre≈°ka u komunikaciji sa bazom"+e);
       }
        clearFields(e);
    }//GEN-LAST:event_loginBtnActionPerformed

